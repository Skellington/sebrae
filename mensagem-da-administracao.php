					<p>O ano de 2021 confirmou o acerto de estratégias adotadas pelo Sebrae Minas para oferecer atendimento amplo, com soluções ágeis e ajustadas às necessidades de nossos clientes, que precisaram adequar rapidamente seus modelos de negócios em resposta às novas demandas do mercado. E também mostrou que estamos no caminho certo ao articular, junto ao poder público e instituições parceiras, ações que ajudem a promover um ambiente cada vez mais propício ao empreendedorismo e aos pequenos negócios nos municípios mineiros.</p>

					<p>Depois de um 2020 de muitas dificuldades por causa da pandemia de Covid-19, conseguimos reagir rapidamente, criando um plano estratégico emergencial para contornar os desafios em nossas duas principais frentes de atuação: o Suporte Empresarial - que abrange todas as soluções de orientação e capacitação do Sebrae Minas para os empreendedores -, quanto o Desenvolvimento Econômico - que integra nossas ações de melhoria do ambiente de negócios. Foi um ano de transição, em que demos os primeiros passos para cumprir a Estratégia 2023.</p> 

					<p>Internamente, adotamos medidas de redução de custos, com negociação dos aluguéis, corte de frota de veículos, redução de salários e jornadas. Junto a isso, nossa rápida guinada para o digital permitiu uma operação positiva em 2021, o que, apesar da perda de arrecadação que tivemos no período, nos possibilitou alcançar um dos nossos principais objetivos desde o início da crise: preservar o emprego dos nossos colaboradores.</p> 

					<p>Com a volta gradual às atividades presenciais, retomamos com nossos colaboradores o desafio comum de construir, dia a dia, uma cultura organizacional mais eficiente. Por isso, uma das nossas metas foi resgatar a ideia de UM SÓ SEBRAE, reforçando o entendimento de que o cliente é a maior razão da nossa existência.</p> 

					<p>E para seguir oferecendo aos nossos clientes soluções efetivas para seus desafios diários, avançamos no fortalecimento de nossa rede de atendimento presencial. Até dois anos atrás, tínhamos 57 agências de atendimento no estado. Mas, graças às parcerias com o Estado e municípios, conseguimos ampliar significativamente nossa presença com os pontos de atendimento de instituições parceiras que estão aptos a oferecer um serviço próximo e eficiente, e se tornou uma verdadeira vitrine da qualidade do nosso trabalho.</p>

					<p>Paralelamente a essa expansão do atendimento presencial, com capilaridade e eficácia, somada às melhorias dos nossos serviços digitais, buscamos gerar receita própria, com margem líquida para enfrentar um eventual corte no orçamento. Foram desenvolvidos e aperfeiçoados produtos como a plataforma Focus Sebrae; o Sebrae Play, com serviços de streaming e webséries, e o Crédito Assistido, que disponibiliza treinamento digital para as pequenas empresas. A resposta veio a contento: em dezembro comemoramos a marca de 1 milhão de downloads do nosso aplicativo, o que confirma a grande demanda por nossas soluções.</p> 

					<p>Também em 2021 reforçamos o nosso compromisso com a promoção dos Objetivos de Desenvolvimento Sustentáveis (ODS) das Nações Unidas, fortalecendo a cultura da sustentabilidade econômica, ambiental e social, além de uma governança estruturada e consciente de suas responsabilidades. Em 2022, pretendemos reforçar nosso compromisso com o Pacto Global e ajudar os municípios a criarem agendas orientadas pelos ODS.</p> 

					<p>Entre os trabalhos alinhados aos ODS está a educação empreendedora, tema em que, aliás, somos pioneiros – Minas Gerais é a única unidade do Sistema Sebrae que tem uma escola de formação gerencial desde 1994.</p> 

					<p>Outra iniciativa importante no período relacionada aos ODS foi o fortalecimento do Programa Gaia – Gestão de Avaliação e Impactos Ambientais. A ação transversal tem como objetivo gerenciar o impacto ambiental de nossas atividades por meio da utilização racional dos recursos naturais e práticas de desenvolvimento sustentável. Também na área de responsabilidade social, as iniciativas do Sebrae de Plurais e Sebrae DELAS ganharam mais reforço.</p> 

					<p>Tudo isso graças ao empenho de nossos colaboradores e parceiros, com o respaldo da confiança que a sociedade mineira deposita em nosso trabalho. Juntos, superamos essa pandemia e os desafios de 2021, e assim seguiremos em 2022: quebrando barreiras para levar Suporte Empresarial e Desenvolvimento Econômico aos quatro cantos dessas Minas Gerais.</p> 
